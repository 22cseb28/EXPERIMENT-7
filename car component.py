#components of car
car=('engine','battery','radiator','brakes','fuel tank')
# slicing
print(car[0])
#count
print('count:',car.count('brakes'))
#index
print('index:',car.index('engine'))
#len
print('len is:',len('car'))
#sorted
print('sorted is:',sorted('car'))
#enumerate
print('enumerate is:',enumerate('car'))
#min value
print('min value:',min(car))
#max value
print('max:',max(car))
#repetition
print('repetition:',car*4)
#concatenation
car_parts=('door','mirror','clutch')
print('concatenaion:',car+car_parts)





